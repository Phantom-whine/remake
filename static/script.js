function change_layout(int){
    if(int == 1){
        box.style.flexDirection = 'row';
        box.style.top = '10px';
    }else if(int == 2){
        box.style.flexDirection = 'row-reverse';
        box.style.top = '10px';
    }else if(int == 3){
        box.style.flexDirection = 'column';
        box.style.height = 'fit-content';
        box.style.top = '1px';
    }else{
        box.style.flexDirection = 'column-reverse';
        box.style.height = 'fit-content';
        box.style.top = '1px';
    }
}

var open = false;
document.querySelector('#trigger').addEventListener('click', ()=>{
    if(open == false){
        document.querySelector('.div-center').style.display = 'block';
        open = true;
    }else{
        document.querySelector('.div-center').style.display = 'none';
        open = false;
    }
})
